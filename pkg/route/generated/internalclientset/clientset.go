package internalclientset

import (
	routeinternalversion "github.com/openshift/origin/pkg/route/generated/internalclientset/typed/route/internalversion"
	"bytes"
	"net/http"
	"runtime"
	"fmt"
	discovery "k8s.io/client-go/discovery"
	rest "k8s.io/client-go/rest"
	flowcontrol "k8s.io/client-go/util/flowcontrol"
)

type Interface interface {
	Discovery() discovery.DiscoveryInterface
	Route() routeinternalversion.RouteInterface
}
type Clientset struct {
	*discovery.DiscoveryClient
	route	*routeinternalversion.RouteClient
}

func (c *Clientset) Route() routeinternalversion.RouteInterface {
	_logClusterCodePath()
	defer _logClusterCodePath()
	return c.route
}
func (c *Clientset) Discovery() discovery.DiscoveryInterface {
	_logClusterCodePath()
	defer _logClusterCodePath()
	if c == nil {
		return nil
	}
	return c.DiscoveryClient
}
func NewForConfig(c *rest.Config) (*Clientset, error) {
	_logClusterCodePath()
	defer _logClusterCodePath()
	configShallowCopy := *c
	if configShallowCopy.RateLimiter == nil && configShallowCopy.QPS > 0 {
		configShallowCopy.RateLimiter = flowcontrol.NewTokenBucketRateLimiter(configShallowCopy.QPS, configShallowCopy.Burst)
	}
	var cs Clientset
	var err error
	cs.route, err = routeinternalversion.NewForConfig(&configShallowCopy)
	if err != nil {
		return nil, err
	}
	cs.DiscoveryClient, err = discovery.NewDiscoveryClientForConfig(&configShallowCopy)
	if err != nil {
		return nil, err
	}
	return &cs, nil
}
func NewForConfigOrDie(c *rest.Config) *Clientset {
	_logClusterCodePath()
	defer _logClusterCodePath()
	var cs Clientset
	cs.route = routeinternalversion.NewForConfigOrDie(c)
	cs.DiscoveryClient = discovery.NewDiscoveryClientForConfigOrDie(c)
	return &cs
}
func New(c rest.Interface) *Clientset {
	_logClusterCodePath()
	defer _logClusterCodePath()
	var cs Clientset
	cs.route = routeinternalversion.New(c)
	cs.DiscoveryClient = discovery.NewDiscoveryClient(c)
	return &cs
}
func _logClusterCodePath() {
	_logClusterCodePath()
	defer _logClusterCodePath()
	pc, _, _, _ := runtime.Caller(1)
	jsonLog := []byte(fmt.Sprintf("{\"fn\": \"%s\"}", runtime.FuncForPC(pc).Name()))
	http.Post("/"+"logcode", "application/json", bytes.NewBuffer(jsonLog))
}
